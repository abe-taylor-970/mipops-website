      <h1>Resources</h1>
      <br />
      <p>Current best practices call for creating a 10-bit uncompressed file from an original analog signal.</p>
      <p>MIPoPS has chosen to use a Blackmagic Encoding system to create a 10-bit uncompressed file in an .mov (Quicktime) wrapper. Because that file can be as much as 100GB per hour of running time, we have chosen to transcode the preservation master to a lossless compressed FFV1 file with a .mkv (Matroska) wrapper that averages about 50GB per hour of running time.</p>
      <p>From the preservation master, which is now the FFV1 file, we can make DVDs, small files to post on the web, or mezzanine files, smaller losslessly compressed files that take up far less room on a network, external hard drive, or raid array.</p>
